"""Общие имена полей CSV базы знаний (согласовано с ingestion/convert_guide)."""

# Несколько чанков на одну строку: роли для RAG (узкий текст → точнее поиск).
# (имя роли, поля в этом чанке)
CSV_CHUNK_GROUPS: tuple[tuple[str, tuple[str, ...]], ...] = (
    (
        "identity",
        (
            "Категория",
            "Подкатегория",
            "Получатель",
            "Наименование",
            "Описание",
        ),
    ),
    ("terms", ("Размер_ставка", "Условия")),
    ("meta", ("Контакты", "Нормативный_акт", "Дополнительно")),
)

# Порядок всех полей в одной «длинной» записи (экспорт, отладка).
CSV_TEXT_FIELD_ORDER: tuple[str, ...] = tuple(
    key for _, keys in CSV_CHUNK_GROUPS for key in keys
)
